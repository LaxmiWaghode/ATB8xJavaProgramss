package sept.ex_25092024Loop;

public class Lab032 {
    public static void main(String[] args) {
        for(int i=10;i>=1;i--){
            System.out.println(i);
        }
        System.out.println();

        for (int i=1;i<=10;i++){
            System.out.println(i);
        }
        System.out.println();

        for (int Dhanu=1;Dhanu<=10;Dhanu++){
            System.out.println(Dhanu);
        }
        System.out.println();


        for (int _1=1;_1<6;_1++){
            System.out.println(_1);
        }
    }
}
